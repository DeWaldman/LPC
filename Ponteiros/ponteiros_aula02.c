#include <stdio.h>

//-----------------------------------------------------------------------------------------------------------------------------------------
int ex01(int i, int *v, int n){
  int a, cont=0;
  int valor = v[i];
  for(a=0;a<n;a++){
    if(v[a] < valor){
        cont++;
    }
  }
return cont;}
//-----------------------------------------------------------------------------------------------------------------------------------------


//-----------------------------------------------------------------------------------------------------------------------------------------
int ex02(int *v, int *w, int n){
  int a;
  for(a=0;a<n;a++){
    w[ex01(a, &v[0], 10)] = v[a];
  }
return 0;}
//-----------------------------------------------------------------------------------------------------------------------------------------




